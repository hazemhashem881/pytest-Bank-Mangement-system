import pickle
import pathlib

class Account :
    accNo = 0
    name = ''
    deposit=0
    type = ''
    

def displaySp(num):


    file = pathlib.Path("accounts.data")
    if file.exists ():
        infile = open('accounts.data','rb')
        mylist = pickle.load(infile)
        infile.close()
        found = False
        for item in mylist :
            if item.accNo == num :
                print("Your account Balance is = ",item.deposit)
                found = True
        if not found :
            print("No record found")
    else :
        print("No records to Search")


